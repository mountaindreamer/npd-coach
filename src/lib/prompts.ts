export function buildFeedbackPrompt(
  scenario: Scenario | { title: string; npdPattern: string },
  messages: { role: string; content: string }[]
): string {
  const conversationLog = messages
    .map(
      (m) => `${m.role === "user" ? "【用户】" : "【NPD角色】"}: ${m.content}`
    )
    .join("\n");

  return `你是一位NPD应对训练的评估专家。请分析以下模拟对话中用户的应对表现。

## 场景信息
- 场景：${scenario.title}
- 操控模式：${scenario.npdPattern}

## 对话记录
${conversationLog}

## 评估要求

请从以下维度进行评估，每项1-10分，并提供具体分析：

1. **边界设定能力**（boundary）：用户是否清晰表达了自己的底线？是否在承认自己合理责任的同时不被对方带偏？
2. **情绪调节能力**（emotional）：用户是否保持了冷静，没有被激怒？但注意：适度表达情绪（如"这让我不舒服"）是健康的，不等于失控。
3. **策略有效性**（strategy）：用户使用的应对策略是否有效？注意：好的应对不只是"不被卷入"，也包括合理表达诉求、坚持事实、拒绝话题转移。如果场景中用户有合理的诉求（比如对方确实做错了），用户坚持指出问题是正确的做法。

请严格按照以下JSON格式回复（不要有其他内容）：
{
  "overallScore": <1-10>,
  "boundaryScore": <1-10>,
  "emotionalRegulation": <1-10>,
  "strategyEffectiveness": <1-10>,
  "strengths": ["<优点1>", "<优点2>"],
  "improvements": ["<改进建议1>", "<改进建议2>"],
  "suggestedResponses": ["<推荐回应方式1>", "<推荐回应方式2>"]
}`;
}
